
10.upto(20){ |i| puts i }      			# this will print numbers from 10 to 20

puts "using downto"
10.downto(1){ |i| puts i }


print "using times"
3.times{ puts "hello" }					# this will print the string 3 times


puts "using step"

1.step(10,3){ |i| puts i }				# it will print the values from 1 to 10 by incrementing the value by 3 i.e. 1 , 4, 7, 10 